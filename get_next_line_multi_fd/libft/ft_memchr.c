#include "libft.h"

void	*ft_memchr(const void *ptr, int c, size_t n)
{
	while (n--)
	{
		if (*(const unsigned char *)ptr == (unsigned char)c)
			return ((void *)ptr);
		ptr = (unsigned char *)ptr + 1;
	}
	return (NULL);
}
