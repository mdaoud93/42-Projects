#include "libft.h"

char	*ft_strrev(char *str)
{
	int		temp;
	int		i;
	int		j;

	i = 0;
	j = ft_strlen(str) - 1;
	while (i <= j)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		i++;
		j--;
	}
	return (str);
}
