#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	char	*temp;

	temp = (char *)s;
	while (*temp && *temp != (char)c)
		temp++;
	if (*temp == (char)c)
		return (temp);
	return (NULL);
}
