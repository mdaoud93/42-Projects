#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	while (n--)
	{
		if ((*(char *)dest = *(const char *)src) == (char)c)
			return ((char *)dest + 1);
		dest = (char *)dest + 1;
		src = (const char *)src + 1;
	}
	return (NULL);
}
